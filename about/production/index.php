<p>«Gardis» является крупнейшим и единственным за Уралом производителем металлических ограждений полного цикла. Предприятие работает по прямым договорам с ведущими российскими производителями металла, что обеспечивает стабильное качество материала и короткие сроки поставки. Весь оцинкованный металлопрокат, поставляемый в компанию, изготовлен на ведущих предприятиях отрасли и соответствует высочайшим стандартам качества, согласно ГОСТ.</p>
<h2>Производство сетчатых панелей</h2>
<p>Сетчатые панели изготавливаются из оцинкованной проволоки. Вначале из проволоки нужного диаметра производятся заготовки: прямолинейные прутки определенного размера. Для этого на нашем предприятии используются правильно-отрезные автоматизированные станки.</p>
<p>Панели изготавливаются методом многоточечной контактной сварки, на современных автоматизированных сварочных станках производства Германии. Технические особенности станка позволяют отрегулировать интенсивность сварочных процессов  так, чтобы добиться прочного соединения металлических прутков и в то же время свести к минимуму разрушение цинкового слоя в области сварочного соединения. В дальнейшем, в процессе подготовки изделия к полимерной окраске, места сварки закрываются конверсионным нанокерамическим покрытием на основе соединений циркония.</p>
<p>Это даёт возможность производить панели, обладающие:</p>
<ol>
<li>высокой прочностью сварного шва,</li>
<li>точной геометрией ячеек,</li>
<li>сохранностью защитного слоя цинкового покрытия в местах сварки,</li>
<li>эстетичным конструктивом и внешним видом, </li>
<li>долгим сроком службы (до 50 лет),</li>
<li>широким ассортиментом.</li>
</ol>
