<h2>Производство столбов</h2>
<p>Столбы Gardis  изготовлены по бессварочной технологии из оцинкованной стали. Рулон оцинкованной стали загружается в прокатной стан, а затем последовательно проходит несколько технологических переходов:</p>
<ol>
<li>профилирование,</li>
<li>формирование замка Seam Lock,</li>
<li>дополнительное клинчевание (уплотнение замка),</li>
<li>отрезка профиля необходимой длины.</li>
</ol>
<p>Использование технологии Seam Lock выгодно отличает столбы Gardis от аналогов, выполненных со сварным швом. Благодаря использованию бесшовной технологии, цинковое покрытие не подвергается разрушению в зоне сварки, что позволяет избежать коррозии.</p>
